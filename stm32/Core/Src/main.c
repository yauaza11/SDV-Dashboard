void Send_Motor_Data_to_Pi(float current_rpm, float target_rpm){
	txHeader.StdId = 0x201;
	txHeader.RTR = CAN_RTR_DATA;
	txHeader.IDE = CAN_ID_STD;
	txHeader.DLC = 8;

	int16_t rpm_val = (int16_t)(current_rpm * 10);
	int16_t target_val = (int16_t)(target_rpm * 10);

	txData[0] = (rpm_val >> 8) & 0xFF;
	txData[1] = rpm_val & 0xFF;
	txData[2] = (target_val >> 8)&0xFF;
	txData[3] = target_val & 0xFF;
	for(int i=4; i<8; i++) txData[i] = 0;

	// Send Message to Pi
	if(HAL_CAN_AddTxMessage(&hcan, &txHeader, txData, &txMailbox) != HAL_OK){

	}

}

float update_PID(PID_Control *pid, float target, float current){
	pid->target_rpm = target;
	pid->current_rpm = current;
	pid->error = pid->target_rpm - pid->current_rpm;

	//P
	float p_out = pid->Kp * pid->error;
	//I
	pid->error_sum += pid->error;
//	if (pid->error_sum > 1000.0f) pid->error_sum = 1000.0f;
//	if (pid->error_sum < -1000.0f) pid->error_sum = -1000.0f;
	float i_out = pid->Ki * pid->error_sum;
	//D
	float d_out = pid->Kd * (pid->error - pid->last_error);
	pid->last_error = pid->error;

	//cal
	pid->output = p_out+i_out+d_out;
	if(pid->output > 999.0f) pid->output=999.0f;
	if(pid->output < 0.0f) pid->output=0.0f;

	return pid->output;
}
// 1ms마다 모터제어 + 모터데이터 전송!
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if(htim->Instance == TIM4){
		int16_t current_cnt = (int16_t)htim2.Instance->CNT;
		int16_t delta = current_cnt - last_cnt;

//		current_rpm = ((float)delta/1632.0f)*6000.0f;
		current_rpm = (float)delta * 3.67647f;
		last_cnt = current_cnt;

		if(accel_pressed){
			target_rpm += 1.0f;
		}
		else if(brake_pressed){
			target_rpm -= 0.8f;
		}
		else{
			target_rpm -= 0.2f;
		}

		// 속도 범위 제한 (최대 120 RPM, 최소 0)
		if (target_rpm > 160.0f) target_rpm = 160.0f;
		if (target_rpm < 0) target_rpm = 0;
		pid_output = update_PID(&motorPID, target_rpm, current_rpm);

		if(target_rpm <= 0.0f && current_rpm<=0.0f){
			pid_output = 0;
			motorPID.error_sum = 0;
		}
		// 모터 정방향설정
		HAL_GPIO_WritePin(GPIOC, IN1_Motor_Pin, GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOC, IN2_Motor_Pin, GPIO_PIN_SET);

		// PID 결과값(0~999 사이)을 PWM 비교 레지스터에 기록
		__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, (uint16_t)pid_output);

		// Send to Pi the Motor Data
		Send_Motor_Data_to_Pi(current_rpm, target_rpm);
	}
}
// Pi로부터 CAN 받는 신호 -> 이제 이걸로 모터 변수에 신호 주는것으로 바꿔야한다!
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan){
	if(hcan->Instance == CAN1){
		if(HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &rxHeader, rxData) == HAL_OK){
			// Brake Logic
			if(rxHeader.StdId == 0x102){
				if(rxData[0] == 1){
					accel_pressed = 0;
					brake_pressed = 1;
				}
				else{
					brake_pressed = 0;
				}
			}
			// Accel Logic
			else if(rxHeader.StdId == 0x101){
				if(rxData[0]==1){
					accel_pressed = 1;
					brake_pressed = 0;
				}
				else{
					accel_pressed = 0;
				}
			}
		}

	}
}

